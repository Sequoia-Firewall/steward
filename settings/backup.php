    () => {
      bootstrap.Modal.getOrCreateInstance(document.getElementById('restoreProgressModal')).show();
      document.getElementById('restoreForm').submit();
    },
    'Yes, Restore'
  );
}

function openResetModal() {
  document.getElementById('resetConfirmInput').value = '';
  document.getElementById('resetSubmitBtn').disabled = true;
  bootstrap.Modal.getOrCreateInstance(document.getElementById('resetModal')).show();
  document.getElementById('resetModal').addEventListener('shown.bs.modal',
    () => document.getElementById('resetConfirmInput').focus(), { once: true });
}

document.addEventListener('DOMContentLoaded', () => {
  document.getElementById('resetConfirmInput').addEventListener('input', function () {
    const match = this.value === 'I understand';
    document.getElementById('resetSubmitBtn').disabled = !match;
    document.getElementById('resetConfirmHidden').value = this.value;
  });
});
