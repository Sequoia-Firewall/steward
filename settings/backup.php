<script>
const BASE_PATH = '<?= BASE_PATH ?>';
function getCsrfToken() {
  const el = document.querySelector('#scheduleForm input[name="csrf_token"]');
  return el ? el.value : '';
}

// ---- Schedule UI --------------------------------------------------------

function updateScheduleUI() {
  const freq = document.getElementById('backup_schedule').value;
  document.querySelectorAll('.schedule-time-row').forEach(el => el.classList.toggle('d-none', freq === 'off'));
  document.querySelectorAll('.schedule-dow-row').forEach(el  => el.classList.toggle('d-none', freq !== 'weekly'));
  document.querySelectorAll('.schedule-dom-row').forEach(el  => el.classList.toggle('d-none', freq !== 'monthly'));
}

document.addEventListener('DOMContentLoaded', () => {
  document.getElementById('backup_schedule').addEventListener('change', updateScheduleUI);
  updateScheduleUI();
  loadStoredBackups();
});

async function saveSchedule() {
  const form = document.getElementById('scheduleForm');
  const data = new FormData(form);
  const btn  = form.querySelector('button[onclick]');
  btn.disabled = true;

  try {
    const res  = await fetch(BASE_PATH + '/settings/backup_schedule_save', { method: 'POST', body: data });
    const json = await res.json();
    const box  = document.getElementById('scheduleStatus');
    box.classList.remove('d-none', 'alert-success', 'alert-warning', 'alert-danger');

    if (!json.ok) {
      box.className = 'mt-3 alert alert-danger py-2';
      box.innerHTML = '<i class="bi bi-x-circle-fill"></i> ' + (json.error || 'Save failed');
      return;
    }

    if (!json.crontab_ok) {
      box.className = 'mt-3 alert alert-warning py-2';
      let msg = '<i class="bi bi-exclamation-triangle-fill"></i> Settings saved, but could not update crontab automatically.';
      if (json.cron_line) {
        msg += '<br>Add this line to the <code>www-data</code> crontab manually (<code>sudo crontab -u www-data -e</code>):'
             + '<pre class="mb-0 mt-2 p-2 bg-dark text-light rounded small"># steward-backup\n' + json.cron_line + '</pre>';
      }
      box.innerHTML = msg;
    } else {
      box.className = 'mt-3 alert alert-success py-2';
      box.innerHTML = json.cron_line
        ? '<i class="bi bi-check-circle-fill"></i> Schedule saved and crontab updated.'
        : '<i class="bi bi-check-circle-fill"></i> Automatic backups disabled.';
    }

    if (!json.dir_ok) {
      box.innerHTML += ' <span class="text-warning fw-semibold">Warning: backup directory is missing or not writable.</span>';
    }
  } catch (e) {
    const box = document.getElementById('scheduleStatus');
    box.className = 'mt-3 alert alert-danger py-2';
    box.innerHTML = '<i class="bi bi-x-circle-fill"></i> Request failed.';
  } finally {
    btn.disabled = false;
  }
}

// ---- Stored Backups -----------------------------------------------------

function fmtSize(bytes) {
  if (bytes < 1024)        return bytes + ' B';
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + ' KB';
  return (bytes / (1024 * 1024)).toFixed(1) + ' MB';
}

async function loadStoredBackups() {
  const box = document.getElementById('storedBackupsList');
  try {
    const res  = await fetch(BASE_PATH + '/settings/backup_list_server');
    const json = await res.json();

    if (!json.dir_ok) {
      box.innerHTML = '<p class="text-muted text-center py-4 mb-0"><i class="bi bi-folder-x me-1"></i>Backup directory does not exist yet.</p>';
      return;
    }
    if (!json.files.length) {
      box.innerHTML = '<p class="text-muted text-center py-4 mb-0"><i class="bi bi-inbox me-1"></i>No stored backups yet. Click <strong>Back Up Now</strong> to create one.</p>';
      return;
    }

    let html = '<table class="table table-sm table-hover mb-0"><thead><tr>'
             + '<th>Date / Time</th><th>Size</th><th class="text-end">Actions</th></tr></thead><tbody>';
    for (const f of json.files) {
      const name = f.name.replace(/'/g, "\\'");
      html += `<tr>
        <td><i class="bi bi-file-earmark-code me-1 text-muted"></i>${f.display}</td>
        <td class="text-muted small">${fmtSize(f.size)}</td>
        <td class="text-end">
          <a href="${BASE_PATH}/settings/backup_download_server?file=${encodeURIComponent(f.name)}"
             class="btn btn-outline-secondary btn-sm me-1" title="Download">
            <i class="bi bi-download"></i>
          </a>
          <button class="btn btn-outline-danger btn-sm" onclick="deleteBackup('${name}')" title="Delete">
            <i class="bi bi-trash3"></i>
          </button>
        </td>
      </tr>`;
    }
    html += '</tbody></table>';
    box.innerHTML = html;
  } catch {
    box.innerHTML = '<p class="text-danger text-center py-3 mb-0">Failed to load backup list.</p>';
  }
}

async function runBackupNow() {
  const btn  = document.getElementById('runNowBtn');
  btn.disabled = true;
  btn.innerHTML = '<span class="spinner-border spinner-border-sm me-1"></span> Running…';

  const data = new FormData();
  data.append('csrf_token', getCsrfToken());

  try {
    const res  = await fetch(BASE_PATH + '/settings/backup_run_server', { method: 'POST', body: data });
    const json = await res.json();
    if (json.ok) {
      await loadStoredBackups();
    } else {
      if (json.status === 'backup_dir_missing') {
        let msg = '<i class="bi bi-exclamation-triangle-fill" style="color:#e6a817"></i> '
                + '<strong>Backup location is not configured.</strong>';
        if (json.can_configure_backup_dir && json.backup_settings_url) {
          msg += ' <a href="' + json.backup_settings_url + '">Configure backup location</a>';
        }
        showToast(msg, 'warning');
        return;
      }
      alert('Backup failed: ' + (json.error || 'Unknown error'));
    }
  } catch {
    alert('Request failed.');
  } finally {
    btn.disabled = false;
    btn.innerHTML = '<i class="bi bi-play-fill"></i> Back Up Now';
  }
}

function deleteBackup(name) {
  appConfirm(
    'Delete Backup',
    'Delete this backup file?',
    name,
    async () => {
      const data = new FormData();
      data.append('file', name);
      data.append('csrf_token', getCsrfToken());

      const res  = await fetch(BASE_PATH + '/settings/backup_delete_server', { method: 'POST', body: data });
      const json = await res.json();
      if (json.ok) {
        await loadStoredBackups();
      } else {
        alert('Delete failed: ' + (json.error || 'Unknown error'));
      }
    },
    'Delete'
  );
}

// ---- Restore / Reset ----------------------------------------------------

function confirmRestore() {
